// 3. Arrays de frutas
let frutas1 = ["maçã", "banana"];
let frutas2 = ["laranja", "uva"];
console.log("Frutas 1:", frutas1);
console.log("Frutas 2:", frutas2);

// a) Concatenar os dois arrays
let todasFrutas = frutas1.concat(frutas2);
console.log("Todas as frutas:", todasFrutas);

// b) Transformar em string separada por "-"
let frutasComoTexto = todasFrutas.join("-");
console.log("Frutas em string:", frutasComoTexto);
